
public class ArithmeticOperations {
	
	// 더하기 - 함수는 퍼블릭으로 만든다!
	public int addition(int x, int y) {
		return x + y;
	}
	
	// 빼기
	public int subtraction(int x, int y) {
		return x - y;
	}
	
	// 곱하기
	public int multiplication(int x, int y) {
		return x * y;
	}
	
	// 나누기
	public int division(int x, int y) {
		return x / y;
	}
	
	// 나머지
	public int remainder(int x, int y) {
		return x % y;
	}
	
}
