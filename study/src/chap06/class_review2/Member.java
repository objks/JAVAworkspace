package chap06.class_review2;

// 회원정보
public class Member {
	String id;
	String name;
	String password;
	String phone;
	String email;
	
	public Member(String id, String name, String password, String phone, String email) {
		this.id = id;
		this.name = name;
		this.password = password;
		this.phone = phone;
		this.email = email;
	}
}
